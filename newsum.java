import java.util.*;
public class newsum{
    public static void main(String args[]) {
        Scanner sc= new Scanner(System.in);
        int low=sc.nextInt();
        int high=sc.nextInt();
        for(int i=low;i>=low;low++){
            if(i>=high){
                System.out.println(i);
            }
        }

    }
}